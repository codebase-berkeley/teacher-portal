// returns an array of components
// param responses = array of text responses
function displayResponses(responses) {
  let componentArray = [];

  for (let i = 0; i < responses.length; i++) {
    let color = i % 2 == 0 ? 'grey' : 'dark-grey';

    componentArray.push(
      <div className={color}>
        <Cell response={responses[i]} />
      </div>
    );
  }

  return componentArray;
}

class CellResponses extends Component {
  render() {
    return (
      <div>
        <p className="cell-responses">
          What was your favorite part of this unit?
        </p>

        <div className="container">{displayResponses(array)}</div>
      </div>
    );
  }
}

